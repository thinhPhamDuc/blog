    }

    /**
     * Determine whether the user can view the model.
     *
     * @param  \App\User  $user
     * @return mixed
     */
    public function view(User $user)
    {
        return $user->checkPermissionAccess('post_view');
    }

    /**
     * Determine whether the user can create models.
     *
     * @param  \App\User  $user
     * @return mixed
     */
    public function create(User $user)
    {
        return $user->checkPermissionAccess('post_create');
    }

    /**
     * Determine whether the user can update the model.
     *
     * @param  \App\User  $user
     * @return mixed
     */
    public function update(User $user, $id)
    {
        $post = Post::find($id);
        if ($user->checkPermissionAccess('post_edit') && $post->author_id === $user->id){
            return true;
        }
        return false;
    }

    /**
     * Determine whether the user can delete the model.
     *
     * @param  \App\User  $user
     * @return mixed
     */
    public function delete(User $user, $id)
    {
        $post = Post::find($id);
        if ($user->checkPermissionAccess('post_delete') && $post->author_id === $user->id){
            return true;
        }
        return false;
    }

